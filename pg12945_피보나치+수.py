def solution(n):
    # 메모이제이션을 위한 배열을 선언하고 초기화합니다.
    # memo[i]는 i번째 피보나치 수를 1234567로 나눈 나머지를 저장합니다.
    memo = [0] * 100001
    
    # 초기 조건을 설정합니다.
    memo[0] = 0  # F(0) = 0
    memo[1] = 1  # F(1) = 1
    
    # 피보나치 수열을 계산하는 반복문입니다.
    for i in range(2, 100001):
        # 피보나치 수열의 정의에 따라, memo[i]는 memo[i-2]와 memo[i-1]의 합입니다.
        # 각 계산 결과는 1234567로 나눈 나머지를 저장하여 값이 너무 커지지 않도록 합니다.
        memo[i] = (memo[i - 2] + memo[i - 1]) % 1234567
    
    # n번째 피보나치 수를 반환합니다.
    return memo[n]